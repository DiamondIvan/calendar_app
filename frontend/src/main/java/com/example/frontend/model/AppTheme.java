package com.example.frontend.model;

/**
 * AppTheme enum defines the available visual themes for the application.
 * 
 * Available themes:
 * - BLUE: Default blue theme with cool tones
 * - DARK: Dark mode theme
 * - LIGHT: Light mode theme
 * - RED: Red accent theme
 * 
 * Used by {@link com.example.frontend.context.ThemeManager} to manage
 * application-wide theming.
 */
public enum AppTheme {
    BLUE, DARK, LIGHT, RED;
}
